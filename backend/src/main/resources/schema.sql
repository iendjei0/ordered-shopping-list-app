CREATE TABLE IF NOT EXISTS USERS(
    id SERIAL PRIMARY KEY,
    username VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS CURRENT_INGREDIENTS(
    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    count INT NOT NULL,
    user_id INT NOT NULL,
    CONSTRAINT ci_user FOREIGN KEY (user_id) REFERENCES USERS(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS SAVED_INGREDIENTS(
    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    order_value INT NOT NULL,
    user_id INT NOT NULL,
    CONSTRAINT si_user FOREIGN KEY (user_id) REFERENCES USERS(id) ON DELETE CASCADE
);